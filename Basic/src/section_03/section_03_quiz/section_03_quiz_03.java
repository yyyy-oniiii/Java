package section_03.section_03_quiz;

public class section_03_quiz_03 {
    public static void main(String[] args) {

        int num = 10;

        System.out.println(num);    // 10
        System.out.println(num++);  // 10
        System.out.println(num);    // 11    
        System.out.println(--num);  // 10
    }
}
