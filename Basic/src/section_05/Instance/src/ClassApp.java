public class ClassApp {
    public static void main(String[] args) {
        
        // math 클래스를 이용해 원주율 알아내기
        System.out.println(Math.PI);

        // 소숫점 내림
        System.out.println(Math.floor(1.5)); 

        // 소숫점 반올림
        System.out.println(Math.ceil(2.5));

    }
}